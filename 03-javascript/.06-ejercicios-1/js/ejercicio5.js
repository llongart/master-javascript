/*
Ejercicio 5:

Tengo una tarta de queso con 16 porciones. Cada porción vale 3.8 euros.

¿Si vendo todas las porciones, cuánto dinero gano en total?
*/

let porciones = 16;
let precio_unitario = 3.8;
let ganancia_total = porciones * precio_unitario;

let mensaje = `La ganancia total al vender todas las pociones es de ${ganancia_total} euros`;

console.log(mensaje);